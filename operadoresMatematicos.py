# Demostración de operadores matemáticos en Python

a = 15
b = 4

print("a = " + str(a))
print("b = " + str(b))

print("Suma: a + b = " + str(a + b))
print("Resta: a - b = " + str(a - b))
print("Multiplicación: a * b = " + str(a * b))
print("División: a / b = " + str(a / b))
print("División entera: a // b = " + str(a // b))
print("Módulo (resto): a % b = " + str(a % b))
print("Exponenciación: a ** b = " + str(a ** b))